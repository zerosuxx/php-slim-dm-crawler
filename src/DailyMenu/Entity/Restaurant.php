<?php

namespace App\DailyMenu\Entity;

/**
 * Class Restaurant
 * @package App\DailyMenu\Entity
 */
class Restaurant
{
    /**
     * @var string
     */
    private $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}